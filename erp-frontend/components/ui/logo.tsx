interface LogoProps {
  className?: string;
  size?: "sm" | "md" | "lg";
  showText?: boolean;
  variant?: "default" | "white";
}

export function Logo({
  className,
  size = "md",
  showText = true,
  variant = "default",
}: LogoProps) {
  const sizeClasses = {
    sm: "h-8 w-8",
    md: "h-10 w-10",
    lg: "h-12 w-12",
  };

  const textSizeClasses = {
    sm: "text-lg",
    md: "text-xl",
    lg: "text-2xl",
  };

  return (
    <div className={cn("flex items-center gap-2.5", className)}>
      <div
        className={cn(
          "relative flex items-center justify-center rounded-xl",
          sizeClasses[size],
          variant === "default" ? "bg-foreground" : "bg-white"
        )}
      >
        {/* Main flow icon - abstract S shape representing flow */}
        <svg
          viewBox="0 0 32 32"
          fill="none"
          xmlns="http://www.w3.org/2000/svg"
          className={cn(
            "w-2/3 h-2/3",
            variant === "default" ? "text-background" : "text-foreground"
          )}
        >
          {/* Top arc */}
          <path
            d="M8 8C8 8 12 4 18 4C24 4 26 8 26 10C26 14 22 16 16 16"
            stroke="currentColor"
            strokeWidth="3"
            strokeLinecap="round"
            fill="none"
          />
          {/* Bottom arc */}
          <path
            d="M24 24C24 24 20 28 14 28C8 28 6 24 6 22C6 18 10 16 16 16"
            stroke="currentColor"
            strokeWidth="3"
            strokeLinecap="round"
            fill="none"
          />
          {/* AI sparkle dots */}
          <circle cx="26" cy="6" r="2" fill="currentColor" />
          <circle cx="6" cy="26" r="2" fill="currentColor" />
        </svg>
      </div>
      {showText && (
        <span
          className={cn(
            "font-bold tracking-tight",
            textSizeClasses[size],
            variant === "white" && "text-white"
          )}
        >
          SmartFlow
        </span>
      )}
    </div>
  );
}

